class NumberDataTypes{

    public static void main(String[] args) {
        
        byte x = 2;
        short y = 67;
        int z = 223;
        long a = 4444445;
        float r = 1.3f;
        double t = 33345.2235;
        
        
        System.out.println(x+y);
        System.out.println(y);
        System.out.println(z);
        System.out.println(a);
        System.out.println(r);
        System.out.println(t);
        
    }
    
}